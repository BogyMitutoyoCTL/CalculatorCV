class Datenbank():
    def __init__(self):
        self.upper_h = 0
        self.lower_h = 0
        self.upper_s = 0
        self.lower_s = 0
        self.upper_v = 0
        self.lower_v = 0
        self.size = 0

    def change_upper_h(self, wert):
        self.upper_h = wert

    def change_lower_h(self, wert):
        self.upper_h = wert

    def change_upper_s(self, wert):
        self.upper_h = wert

    def change_lower_s(self, wert):
        self.upper_h = wert

    def change_upper_v(self, wert):
        self.upper_h = wert

    def change_lower_v(self, wert):
        self.upper_h = wert

    def change_size(self, wert):
        self.size = wert

